import turtle as t
t.shape("turtle")
t.speed(0)
t.bgcolor("black")
t.color("green")
for x in range (150):
    t.forward(x)
    t.left(90)
    t.forward(x)
    t.left(90)
    t.forward(x)
    t.right(90)
    t.forward(x)
    t.left(90)
    t.forward(x)
    t.left(90)
    t.forward(x)
    t.right(90)
    t.forward(x)
    t.left(90)
    t.forward(x+x)

    
